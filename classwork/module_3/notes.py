# List
# Lists are similar to arrays where objects or eleents are contained 
# Example:

my_list = [1, 2, 3, 4]

# Append method
# This is very equally the same to push in javascript
# Example:

my_list.append(10)
print(my_list)

# Insert Method
# Injects an element into the list according to location
# Example:

my_list.insert(3, 12)
print(my_list)

