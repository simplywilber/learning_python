# This is a practice using the slicing and find methods

site_string = "http://en.wikipedia.org/wiki/Nasa/"

print(f"The answer for a is: {site_string[10:19]}")
print(f"The answer for b is: {site_string[10:-5]}")
print(f"The answer for c is: {site_string[:23]}")
print(f"The answer for d is: {site_string[:-1]}")

